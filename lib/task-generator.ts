export function getRandomCategory() {
  return categories[randomInt(categories.length)];
}

export function getRandomDifficulty() {
  return difficulties[randomInt(difficulties.length)];
}

export function getLocalPrompt(category: Category, difficulty: Difficulty) {
  const options = templates[category][difficulty];
  return options[randomInt(options.length)];
}
